# variable declarations
x = True
y = False
z = True

# expression
x_and_y = x and y
x_and_z = x and z
x_or_y = x or y
not_x = not x

# output
print(f"x: {x}")
print(f"y: {y}")
print(f"z: {z}")
print("---")
print(f"{x} and {y}: {x_and_y}")
print(f"{x} and {z}: {x_and_z}")
print(f"{x} or {y}: {x_or_y}")
print(f"not_{x}: {not_x}")