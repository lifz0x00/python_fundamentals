# variable declarations
x = 10
y = 4

# expression
multiplication = x * y
exponentiation = x ** y
division = x / y
floor_division = x // y
addition = x + y
subtraction = x - y
modulus = x % y

# output
print(f"{x} * {y}: {multiplication}")
print(f"{x} ** {y}: {exponentiation}")
print(f"{x} / {y}: {division}")
print(f"{x} // {y}: {floor_division}")
print(f"{x} + {y}: {addition}")
print(f"{x} - {y}: {subtraction}")
print(f"{x} % {y}: {modulus}")
