'''
set: unordered(can't get index position), unchangable, unique(can't duplicate), 
'''
# set
set_fruits = {'Apple', 'Banana', 'Cherry', 'Durian'}
print(f"{set_fruits}")

# build set from list
set_phone = set(['Android', 'iOS'])
print(f"{set_phone}")
# print(f"{set_phone[0]}") error, because set unordered

# set with tuple
set_mix = {'Human', 'Animal', 7 , True, ('A', 'B')}
print(f"{set_mix}")

