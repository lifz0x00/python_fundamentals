# variable declarations
x = 10
y = 4

# expression
equal = x == y
not_equal = x != y
greater_than = x > y
less_than = x < y
greater_than_or_equal_to = x >= y 
less_than_or_equal_to = x <= y

# output
print(f"{x} == {y}: {equal}")
print(f"{x} != {y}: {not_equal}")
print(f"{x} > {y}: {greater_than}")
print(f"{x} < {y}: {less_than}")
print(f"{x} >= {y}: {greater_than_or_equal_to}")
print(f"{x} <= {y}: {less_than_or_equal_to}")