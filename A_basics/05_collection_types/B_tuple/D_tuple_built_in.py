# tuple (unchangable) 
tuple_values = (70, 80, 90, 100)
print(f"Values: {tuple_values}")

# tuple built-in functions
print(f"Max   : {max(tuple_values)}") # finds the largest value in a tuple
print(f"Min   : {min(tuple_values)}") # finds the smallest value in a tuple
print(f"Length: {len(tuple_values)}") # Counting the number of items on a tuple