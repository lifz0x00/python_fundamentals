'''
list: ordered, changeable
'''

# list [changeable]
fruits = ["apple", "banana", "cherry", "durian"]
print(f"{fruits}")

# list index
print("---")
print("positive")
print(f"index  0: {fruits[0]}")
print(f"index  1: {fruits[1]}")
print(f"index  2: {fruits[2]}")
print(f"index  3: {fruits[3]}")

print("---")
print("negative")
print(f"index -1: {fruits[-1]}")
print(f"index -2: {fruits[-2]}")
print(f"index -3: {fruits[-3]}")
print(f"index -4: {fruits[-4]}")